#Creating empty variable to store a random word out of a list "word_list".
chosen_word = ""
#Assigning random word to an empty variable.
chosen_word = random.choice(word_list)

#Empty list that will store blanks.
display = []
#For loop to assign blank space to an empty list "display" depending on the lenght of the word.
for i in range(len(chosen_word)):
    display.append("_")

#Variable that will stop the game once turned into True
end_of_game = False
#Variable tracking lives
lives = 6

#While loop for the game to continue until end_of_game is True  
print(logo)
while end_of_game == False:
    guess = input("Guess the letter!: ").lower() #Asking user for input

    if guess in display:
            print("\nYou have already guesses this letter!")

#Number if times it will loop depending on the chosen word.
    for n in range(len(chosen_word)): 
        letter = chosen_word[n] #Creating variable letter that will store position of current letter being looped.
        if letter == guess: #Condition
            print("\nCorrect letter!\n")
            print(stages[lives]) #Print ASCII art
            display[n] = letter #Replacing blank space with a letter currently being looped through.

#Losing if statement
    if guess not in chosen_word: #If user guesses wrong
        lives -= 1
        print(f"Letter {guess} is not in the word, you lost 1 life out of {lives}.")
        print(stages[lives]) #Print ASCII art
        if lives <= 0: #If lives are 0 its end of the game
            print("You lose!")
            end_of_game = True

    print(f"{' '.join(display)}")  #Printing outcome

#Winning if statement
    if "_" not in display: #Once all blanks are gone from display it will stop the game
        print("You win!")
        end_of_game = True  

     
    










